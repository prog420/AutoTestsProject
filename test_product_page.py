from .pages import ProductPage, LoginPage, BasketPage
import time
import pytest
from selenium.webdriver.remote.webdriver import WebDriver


def test_guest_can_add_product_to_basket(browser: WebDriver):
    link = "http://selenium1py.pythonanywhere.com/catalogue/coders-at-work_207/"
    product_page = ProductPage(browser, link)
    product_page.open()
    product_page.should_be_add_to_basket_button()
    product_page.add_product_to_basket()
    product_page.should_be_success_alert_with_prod_title()
    product_page.should_be_info_alert_with_busket_price()


